EXPECTED_HEADERS = [
    "序号", "项目阶段", "对应外包管理重点", "执行事项", "任务属性", "适用性判断及触发条件/场景",
    "责任人", "应形成/提交材料", "制度/管理截止要求", "进度", "完成/关闭条件", "制度依据",
]


def main() -> None:
    if LEGACY_TEMP.exists():
        LEGACY_TEMP.unlink()
    if not PLAN.exists():
        raise FileNotFoundError(PLAN)
    workbook = load_workbook(PLAN)
    sheet = workbook.active
    headers = [sheet.cell(1, column).value for column in range(1, 13)]
    if headers != EXPECTED_HEADERS:
        raise RuntimeError(f"执行计划表头与预期不一致：{headers}")

    row_by_task = {}
    for row in range(2, sheet.max_row + 1):
        task_id = sheet.cell(row, 1).value
        if isinstance(task_id, int):
            row_by_task[task_id] = row
    missing = sorted((set(BASIS_UPDATES) | set(OTHER_UPDATES)) - set(row_by_task))
    if missing:
        raise RuntimeError(f"未找到执行事项序号：{missing}")

    progress_before = {task_id: sheet.cell(row, 10).value for task_id, row in row_by_task.items()}
    for task_id, basis in BASIS_UPDATES.items():
        sheet.cell(row_by_task[task_id], 12).value = basis
    for task_id, columns in OTHER_UPDATES.items():
        row = row_by_task[task_id]
        for column, value in columns.items():
            if column == 10:
                raise RuntimeError("不允许修改J列进度")
            sheet.cell(row, column).value = value

    progress_after = {task_id: sheet.cell(row, 10).value for task_id, row in row_by_task.items()}
    if progress_before != progress_after:
        raise RuntimeError("J列进度发生变化，停止保存")

    if not BACKUP.exists():
        shutil.copy2(PLAN, BACKUP)
    workbook.save(TEMP)
    # Re-open before replacing the user's workbook so binary corruption cannot
    # destroy the working copy.
    check = load_workbook(TEMP, read_only=True, data_only=False)
    check_sheet = check.active
    if check_sheet.max_row != sheet.max_row or check_sheet.max_column != sheet.max_column:
        raise RuntimeError("临时工作簿结构校验失败")
    check.close()
    os.replace(TEMP, PLAN)
    print({
        "plan": str(PLAN),
        "backup": str(BACKUP),
        "basis_updates": len(BASIS_UPDATES),
        "content_updates": len(OTHER_UPDATES),
        "progress_cells_preserved": len(progress_before),
    })


if __name__ == "__main__":
    main()
